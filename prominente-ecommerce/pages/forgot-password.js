import Head from "next/head";
import Link from "next/link";
import { useState } from "react";
import {
  FormLayout,
  FormHeading,
  FormInput,
  FormFooter,
  FormControl,
  FormHeroImage,
} from "../src/components/general/formComponents";
import withPublic from "../src/utils/routes/withPublic";

function ForgotPassword({ authContext }) {
  const { resetPassword, resetPasswordSuccess, resetPasswordError } =
    authContext;

  const [email, setEmail] = useState("");

  async function handlePasswordReset(e) {
    e.preventDefault();
    await resetPassword(email);
  }

  return (
    <div>
      <Head>
        <title>Forgot Password | Prominente</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/vercel.svg" />
      </Head>
      <div className="flex">
        <FormHeroImage logoPos="top-8 left-8" />
        <FormLayout>
          <FormHeading
            header="Forgot Password?"
            subheader="Enter your email to receive further instructions."
          />
          <FormControl onSubmit={handlePasswordReset}>
            <FormInput
              fieldLabel="Email Address"
              type="email"
              onChange={(e) => setEmail(e.target.value)}
            />
            <button className="btn-primary-filled" type="submit">
              Submit
            </button>
            {resetPasswordError && (
              <p className="text-red-600">{resetPasswordError}</p>
            )}
            {resetPasswordSuccess && (
              <p className="text-teal-500">{resetPasswordSuccess}</p>
            )}
            <Link href="/login">
              <a className="hover:underline text-center">
                Log in to your account
              </a>
            </Link>
          </FormControl>
          <FormFooter
            footer="Don't have an account?"
            href="/signup"
            type="Sign Up"
          />
        </FormLayout>
      </div>
    </div>
  );
}

export default withPublic(ForgotPassword);
